                  >en savoir plus</a
                >). <br />
                Un avenant au bail doit être rédigé entre les colocataires qui
                restent et celui qui arrive, afin de libérer l’ancien de ses
                obligations et faire apparaître le nom du nouveau colocataire.
              </p>
