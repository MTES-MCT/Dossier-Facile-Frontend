  <BlogArticle title="Passoires thermiques : top départ du gel des loyers" date="5 Janvier 2023">
    <p>
      Depuis le 24 août 2022, les propriétaires bailleurs de logements classés F et G (consommation
      énergétique comprise entre 331 et 420 kWh/m²/an pour les F, et au-dessus pour les G)
      communément appelés
      <span class="fr-text--bold">« passoires énergétiques »</span>, sont désormais obligés de
      réaliser des travaux s’ils veulent augmenter les loyers à la signature d’un nouveau bail, lors
      du renouvellement du bail ou dans le cadre d’une révision annuelle même prévue au contrat (cf.
      <a href="https://www.legifrance.gouv.fr/jorf/article_jo/JORFARTI000043957099"
        >Article 159 de la Loi Climat et Résilience</a
      >
      de 2021).
    </p>

    <p>
      <img alt="" class="img-center" src="../../assets/images/blog-article15.webp" />
    </p>

    <h2>Pourquoi cette mesure ?</h2>
    <p>
      Les <strong>émissions françaises de CO2</strong> ont atteint 300 000 kt en 2019 dont 55 300 kt
      sont liées au chauffage résidentiel soit 18% des émissions françaises. Cette mesure vise donc
      à <strong>réduire</strong> les émissions de CO2 dans le<strong> secteur résidentiel</strong>
      en incitant les propriétaires bailleurs à <strong>rénover</strong> leur(s) bien(s).
    </p>
    <p>
      Cette mesure vise aussi à
      <strong>protéger les locataires</strong> de la précarité énergétique (factures d'énergie trop
      élevées, renoncement à se chauffer).
    </p>
    <p>
      Selon l'<strong>Observatoire national de la rénovation énergétique</strong> (ONRE), plus
      de<strong> 7 millions</strong> de logements en France sont potentiellement concernés par cette
      mesure. Il y aurait en effet en France au 1er janvier 2022
      <a
        href="https://www.statistiques.developpement-durable.gouv.fr/le-parc-de-logements-par-classe-de-performance-energetique-au-1er-janvier-2022-0"
        >5,2 millions de logements en résidence principale classés catégorie F et G </a
      >(logement dont la consommation est la plus élevée), ce qui représente 17 % du parc des
      résidences principales, auxquelles s'ajoutent 1,2 million de logements en résidence secondaire
      et<strong> 800 000 logements considérés comme vacants</strong>.
    </p>
    <p>
      <strong>À savoir :</strong> le diagnostic de performance énergétique (DPE) doit être remis au
      locataire lors de la
      <a href="https://www.legifrance.gouv.fr/eli/decret/2022/7/29/TREL2214373D/jo/texte"
        >conclusion du bail</a
      >. Il doit avoir été réalisé depuis moins de 10 ans.
    </p>

    <h2>Le calendrier des mesures</h2>
    <p>
      Cette mesure est la première d'une série de mesures visant à inciter les propriétaires à
      rénover leur(s) bien(s). Le gouvernement n'a pas souhaité
      <strong>interdire</strong> tout de suite la location des &ldquo;passoires énergétiques&rdquo;
      mais a mis en place un calendrier dont l'objectif est de préserver le pouvoir d'achat des
      locataires tout en laissant le temps aux propriétaires bailleurs de faire les travaux de
      rénovation nécessaires. Ce calendrier s'étend sur plusieurs années :
    </p>
    <ul>
      <li>
        1er janvier 2023 : interdiction de louer les logements les plus énergivores (consommation
        énergétique supérieure à 450 kWh/m&sup2;/an)
      </li>
      <li>1er janvier 2025 : interdiction de louer les logements de catégorie G.</li>
      <li>1er janvier 2028 : interdiction de louer les logements de catégorie F</li>
      <li>1er janvier 2034 : interdiction de louer les logements de catégorie E</li>
    </ul>
    <p>
      Concrètement, ces logements entreront dans la catégorie des
      <strong>logements indécents</strong> et il ne sera par conséquent plus possible de les louer.
    </p>
    <p>
      Attention cependant, cette interdiction ne s'appliquera pas sur les locations en cours. Ainsi
      un locataire pourra continuer à louer un bien de catégorie G par exemple si son bail est signé
      avant 2025.
    </p>
    <p>
      Grâce à ces mesures incitatives, le gouvernement vise un objectif de plus de 4 millions de
      logements rénovés d'ici 2034.
    </p>

    <h2>Que faire si je vis dans une passoire thermique ?</h2>
    <p>
      Comme indiqué précédemment, dès 2023, la notion de logement décent tiendra compte de la
      consommation énergétique, et exclura progressivement les biens trop gourmands en énergie. Or,
      la location d'un logement indécent est
      <strong>sanctionnée par la Loi</strong>. Comme expliqué sur
      <a href="https://www.service-public.fr/particuliers/vosdroits/F2042">service-public</a>.fr :
      <em
        >&laquo; si le juge du tribunal constate que le logement ne satisfait pas aux normes de
        décence, il peut contraindre le propriétaire à faire les travaux nécessaires &raquo;.</em
      >
    </p>
    <p>
      La
      <a
        href="https://www.vie-publique.fr/loi/278460-loi-22-aout-2021-climat-et-resilience-convention-citoyenne-climat"
        >loi Climat</a
      >
      confirme ce point :
      <em
        >&laquo; Le locataire peut exiger de son propriétaire qu'il effectue des travaux et
        plusieurs mécanismes d'information, d'incitation et de contrôle viendront renforcer ce droit </em
      >&raquo;.
    </p>
    <p>
      Ainsi, un locataire concerné est en droit d'exiger des travaux à son propriétaire. Ce dernier
      ne peut pas les compenser en rehaussant son loyer.
    </p>
    <p>
      A savoir : Pour lutter contre la précarité énergétique, le gouvernement a également mis en
      place un
      <a href="https://www.service-public.fr/particuliers/vosdroits/F33667">chèque énergie</a>,
      d'une valeur de 100 à 200&euro;, valable jusqu'au 31 mars 2023, sous condition de ressources,
      pour aider les ménages les plus modestes à
      <strong>payer leurs factures d'énergie et achat de combustible</strong>
      mais aussi, à faire
      <strong>certains travaux ou </strong>régler certaines<strong>
        dépenses énergétiques pour leur logement. </strong
      >Il n'y a rien à faire pour en bénéficier, le chèque sera automatiquement envoyé aux ménages
      éligibles.
    </p>

    <h2>Des sanctions renforcées pour les annonces mensongères !</h2>
    <p>
      Depuis 2021, le diagnostic de performance énergétique (DPE) n'est plus seulement informatif
      mais
      <strong>opposable, </strong>c'est-à-dire qu'il engage la responsabilité du propriétaire
      vis-à-vis du futur locataire. Le locataire peut donc se retourner vers le propriétaire si
      celui-ci se révèle inexact.
    </p>
    <p>
      De fait, un propriétaire qui loue une passoire thermique avec une
      <strong>annonce mensongère</strong>, que ce soit volontaire ou non, s'expose à une double
      peine une fois l'interdiction en vigueur. L'acquéreur ou le locataire peut faire un recours
      auprès du tribunal pour demander des dommages et intérêts voire l'annulation de la vente ou du
      bail si l'annonce publiée par voie de presse écrite, sur internet ou affichée en agence
      comporte de fausses informations.
    </p>
    <p>Le juge pourra alors imposer au bailleur de :</p>
    <ul>
      <li>mettre en conformité son logement</li>
      <li>réduire le loyer</li>
      <li>payer des dommages et intérêts au locataire.</li>
    </ul>
    <p>Vous avez donc plusieurs recours possibles si vous louez un logement énergivore.</p>
    <p>
      Enfin, et si malgré tout un déménagement s'impose, n'oubliez pas de vérifier le
      <strong>DPE</strong> sur les annonces immobilières et de faire votre dossier de location avec
      <a href="https://www.dossierfacile.logement.gouv.fr/"
        ><em>Dossier</em><strong>Facile</strong></a
      ><strong>.</strong>
    </p>
