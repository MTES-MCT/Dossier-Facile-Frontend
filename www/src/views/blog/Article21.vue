    <p class="fr-text--lead">
      La recherche d'un logement constitue l'une des étapes cruciales de votre
      parcours universitaire. Chez Dossier<strong>Facile</strong>, nous en avons
      conscience et nous sommes dévoués à vous faciliter cette démarche. Voici
      un guide exhaustif des aides publiques (État, collectivités, autres
      organismes publics) disponibles en France pour t’accompagner dans ta quête
      de logement.
    </p>
    <img
      alt="Une carte d'identité avec un filigrane"
