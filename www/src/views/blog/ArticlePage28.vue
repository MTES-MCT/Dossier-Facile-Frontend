        comme « PAP », « Jinka », « SeLoger », ou encore des groupes dédiés sur les réseaux sociaux.
      </li>
      <li>
        <strong>Consultez le CROUS</strong> : Les logements gérés par le CROUS sont généralement
        plus abordables et adaptés aux étudiants étrangers.
