        >
        : une aide financière versée par la CAF pour réduire le montant de votre loyer.
      </li>
      <li>
        <a
          href="https://www.service-public.fr/particuliers/vosdroits/F1280"
          target="_blank"
          rel="noopener"
          ><strong>Allocation logement sociale (ALS)</strong></a
        >
        : une autre aide versée selon vos ressources.
