    <h2>4. Commencer vos recherches à l'avance</h2>
    <p>
      Le timing est crucial dans la recherche de logement. En France, les logements disponibles
      partent souvent très vite, surtout dans les zones étudiantes.
    </p>

    <h3>Nos conseils pour réussir vos recherches :</h3>
    <ul>
      <li>
        <strong>Commencez tôt</strong> : Idéalement, deux à trois mois avant la date de votre
        arrivée.
