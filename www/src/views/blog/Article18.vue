    <br />

    <h2 class="fr-h6">Garant ou caution ?</h2>
    <p>
      On emploie le terme de <strong>garant</strong> mais en droit, le terme
      exact est celui de <strong>caution</strong>. La caution est l’acte par
      lequel une personne accepte d’assumer à la place du locataire la charge du
      loyer ou les réparations si celui-ci fait défaut ou détériore le bien.
    </p>
    <p>
      Le défaut de paiement étant la plus grande peur d’un propriétaire, cet
      élément du dossier est déterminant et peut faire changer votre dossier de
      pile !
    </p>
    <p>
      Il existe trois types de garant : le garant physique, le garant moral et
      l’organisme de garantie.
    </p>
    <h2 class="fr-h6">Le garant physique</h2>
    <p>
      Il s’agit d’une vraie personne : un parent, un ami, un proche… mais en
      théorie, <strong>n’importe qui</strong>. Assurez-vous néanmoins que :
    </p>
    <ul>
      <li>
        il <strong>gagne plus que vous</strong> : sinon il est probable qu’il ne
        puisse pas vraiment vous aider si vous faites défaut,
      </li>
      <li>
        il <strong>ait un emploi stable</strong> (périodes d’essai ou CDD à
        éviter),
      </li>
      <li>
        il <strong>soit lui-même propriétaire</strong> (si possible) : un
        deuxième loyer à payer pourrait être lourd à supporter.
      </li>
    </ul>
    <p>
      Le garant physique idéal est quelqu’un d’installé professionnellement et
      qui gagne aisément sa vie. Souvent, ce sont les
      <strong>parents</strong> ou les <strong>grands-parents</strong> qui se
      portent garants pour leurs enfants quand ils emménagent pour la première
      fois.
    </p>
    <p>
      Le garant moral joue le même rôle que le garant physique, mais il s’agit
      d’une personne morale : une entreprise, une association…
    </p>
    <h2 class="fr-h6">Les organismes de garantie</h2>
    <p>
      <a href="https://www.visale.fr/" target="_blank" rel="noopener">Visale</a>
      <strong> est une caution entièrement gratuite </strong> d’Action Logement,
      qui a pour mission d’aider gratuitement les jeunes et les personnes les
      plus précaires à accéder à la location.
      <strong
        >Nous vous la recommandons fortement si vous êtes éligible !</strong
      >
    </p>
    <p>
      Il existe des services alternatifs privés mais qui sont payants pour les
      locataires, qu’on trouve aussi sous le nom de “GLI inversées” (voir
      paragraphe GLI plus bas). On ne vous les recommande pas si vous êtes
      éligible à Visale !
    </p>
    <h2 class="fr-h6">Garants multiples, bail solidaire et colocation</h2>
    <p>Les responsabilités du garant varient en fonction du type de bail.</p>
    <ul>
      <li>
        Dans un <strong>bail solidaire</strong>, les garants sont collectivement
        responsables de toutes les obligations du locataire. Si l’un des garants
        ne remplit pas ses obligations, les autres garants devront se
        substituer.
      </li>
      <li>
        Dans un <strong>bail non solidaire</strong>, chaque garant n'est
        responsable que de sa part dans les obligations du locataire. Si le
        locataire a deux garants, chacun n’est responsable que de la moitié des
        paiements dus et n’aura pas à se substituer en cas de défaut d’un des
        autres garants.
      </li>
      <li>
        Dans un <strong>bail de colocation commun</strong>, le garant est
        responsable de l'ensemble des colocataires, mais si chaque colocataire
        signe un <strong>bail individuel</strong>, le garant peut ne se porter
        caution que pour le colocataire qu'il a choisi d’assurer.
      </li>
      <li>
        Il arrive que le
        <strong
          >propriétaire exige un seul garant pour l'ensemble des
          colocataires</strong
        >, même si chacun a signé un bail individuel. Le garant sera alors
        responsable vis-à-vis de tous les colocataires.
      </li>
    </ul>
    <p>
      👉 Dans tous les cas,
      <strong
        >il est crucial que les garants connaissent les termes du bail et
        comprennent leurs obligations avant de s'engager,</strong
      >
      et
      <strong
        >s’assurent au préalable de pouvoir supporter les risques
        financiers</strong
      >
      associés.
    </p>
    <h2 class="fr-h6">La garantie loyer impayés (GLI)</h2>
    <p>
      Le propriétaire peut de son côté souscrire à une
      <strong>assurance</strong> ou
      <strong>une garantie loyers impayés (GLI) payante</strong>
      contre les loyers impayés de son locataire. De nombreuses GLI exigent que
      le locataire :
    </p>
    <ul>
      <li>soit titulaire d’un CDI et gagne 2,7 fois le montant du loyer</li>
      <li>
        soit titulaire d’un CDD depuis plus de 8 mois et gagne 3 fois le montant
        du loyer
      </li>
      <li>s’il est étudiant, présente une caution solidaire d’un tiers</li>
    </ul>
    <p>
      Ces critères ne sont pas définis par la loi mais représentatifs des
      pratiques commerciales observées sur le marché.
    </p>
    <p>
      Dans le cas d’une colocation, la GLI est souscrite pour l’ensemble des
      colocataires. Toutefois, la couverture de l'assurance peut varier en
      fonction de la politique de l'assureur
    </p>
    <p>
      De nombreux services de caution payante pour les locataires fleurissent
      sur le marché, on les appelle
      <strong>“GLI inversées”</strong>.
    </p>
    <p>
      En colocation et bail individuel, chaque colocataire peut souscrire une
      GLI inversée, qui prendra alors en charge ses impayés et non ceux des
      autres colocataires.
    </p>
    <p>
      👉 Dans tous les cas, il est important que le locataire et le propriétaire
      vérifient les termes de l'assurance avant de la souscrire et s'assurent
      qu'elle offre une couverture adéquate pour les risques liés à la
      colocation.
    </p>
    <p>
      🚫 Il est <strong>interdit</strong> pour un propriétaire de
      <strong>cumuler GLI et demande de garants</strong> à son ou ses
      locataires. Cette règle s’applique également à la garantie Visale. Le
      cumul GLI et caution est toutefois autorisé uniquement dans le cas où le
      locataire est étudiant ou apprenti.
    </p>
    <h2 class="fr-h6">📂 Comment intégrer son garant à son DossierFacile ?</h2>
    <p>
      Rien de plus facile ! Après l’ajout de ses pièces personnelles, le
      candidat locataire a la possibilité d’ajouter un garant à son dossier : 
    </p>
    <ul>
      <li>
        <strong>Un garant physique classique</strong> : les documents à fournir
        sont les mêmes que ceux du candidat, mais les pièces acceptées sont un
        peu plus souples.
      </li>
      <li>
        <strong>Un garant organisme</strong> : la pièce à fournir est une
        attestation de garantie délivrée par l’organisme de garantie
        (attestation Visale par exemple…)
      </li>
      <li>
        <strong>Un garant moral</strong> : fournir une une attestation de
        garantie émise par la personne morale, ainsi qu’une pièce d’identité du
        représentant légal de la personne morale
      </li>
      <li>
        <strong>Pas de garant ?</strong> Rien n’oblige un candidat locataire à
        avoir un garant. Néanmoins si vous ne connaissez personne pour se porter
        garant pour vous, on rappelle (encore une fois !) que
        <a href="https://www.visale.fr/" target="_blank" rel="noopener"
          >la garantie Visale</a
        >
        est gratuite. Alors, pourquoi ne pas en profiter ?
      </li>
    </ul>
