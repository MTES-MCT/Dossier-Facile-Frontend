      concurrence est rude et vous désespérez de trouver un nouveau logement ?
      Au cours de vos recherches, vous consultez une annonce immobilière pour la
      location d’un appartement qui correspond parfaitement à vos critères. Qui
      plus est, le prix de la location est attractif. Vous répondez donc à
      l’annonce.
    </p>
    <p>
      Le prétendu bailleur vous demande alors de transmettre votre dossier
      locatif par email pour faire une “présélection des dossiers”.
    </p>
    <p>
      <b>Une règle fondamentale</b>
      : ne jamais transmettre de documents personnels sans être certain de
      l’identité de votre interlocuteur.
    </p>

    <p>
      <b>Notre conseil</b>
      : ne pas répondre à des annonces trop alléchantes ou qui vous paraissent
      suspectes.
    </p>

