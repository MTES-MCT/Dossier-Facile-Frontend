      d'argent pour réserver le logement, puis à ne plus donner signe de vie. Mais d’autres
      arnaqueurs ciblent plutôt leurs victimes à partir du dossier locataire envoyé avant la visite.
      Dans ce cas, il peut être long de se rendre compte qu’on a été victime d’usurpation d'identité
      !
    </p>
