    <h2>L’usurpation d’identité : c’est quoi ?</h2>
    <p>
      <b
        >L’usurpation d’identité consiste à utiliser, sans votre accord, des informations permettant
        de vous identifier.
      </b>
      Il peut s'agir, par exemple, de vos nom et prénom, de votre adresse électronique, ou encore de
      photographies.
    </p>
    <p>
      C’est un problème qui touche
      <b>plusieurs centaines de milliers de Français</b> chaque année pour un total de
      <b>474 millions d’euros extorqués</b>.
    </p>
    <p>
      Ces informations peuvent ensuite être utilisées à votre insu, notamment pour souscrire sous
      votre identité un crédit, un abonnement, commettre des actes répréhensibles ou nuire à votre
      réputation.
    </p>
    <p>
      C’est un délit, qui est donc <u>puni par la loi</u> et la personne qui usurpe une identité
      <b>risque jusqu’à 1 an de prison et 15.000 euros d’amende</b>.
    </p>

    <h2>L’usurpation d’identité et la location : un lien de cause à effet</h2>
    <p>
      Rien n’est plus simple qu’une fausse annonce sur un site d’annonces immobilières pour qu’une
      personne mal intentionnée profite de votre dossier de location pour vous soutirer des
      documents personnels.
    </p>
    <p>
      On constate d’ailleurs, une flambée des arnaques concernant les locations immobilières sur
      internet. Ainsi pas moins de 7 000 signalements sur Pharos (la plateforme officielle de
      signalement des comportements illicites) ont été fait depuis le mois de janvier 2022 dont plus
      de 600 concernaient de
      <b>fausses locations</b>.
    </p>
    <p>
      Aujourd’hui, lors d’un dépôt de plainte pour usurpation d’identité, il n’est pas rare que la
      police demande à la victime si elle a récemment constitué et envoyé un dossier de location…
    </p>

    <h2>Comment faire pour éviter ça ?</h2>
    <p>
      Vous êtes à la recherche d’un appartement dans une zone tendue ? La concurrence est rude et
      vous désespérez de trouver un nouveau logement ? Au cours de vos recherches, vous consultez
      une annonce immobilière pour la location d’un appartement qui correspond parfaitement à vos
      critères. Qui plus est, le prix de la location est attractif. Vous répondez donc à l’annonce.
    </p>
    <p>
      Le prétendu bailleur vous demande alors de transmettre votre dossier locatif par email pour
      faire une “présélection des dossiers”.
    </p>
    <p>
      <b>Une règle fondamentale</b>
      : ne jamais transmettre de documents personnels sans être certain de l’identité de votre
      interlocuteur.
    </p>

    <p>
      <b>Notre conseil</b>
      : ne pas répondre à des annonces trop alléchantes ou qui vous paraissent suspectes.
    </p>

