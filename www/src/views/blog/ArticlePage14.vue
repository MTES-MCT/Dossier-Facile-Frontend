    <h2>Qu'est-ce qu'une donnée personnelle ?</h2>
    <p>
      Une « donnée personnelle » désigne « toute information se rapportant à une personne physique
      identifiée ou identifiable ». Par exemple :
    </p>
    <ul>
      <li>un nom et prénom</li>
      <li>un identifiant (numéro client, numéro de téléphone...)</li>
      <li>une caractéristique (sexe, âge, revenu, état de santé...)</li>
    </ul>
    <p></p>

    <h2>Qu'est-ce qu'un traitement de données personnelles ?</h2>
    <p>
      Un traitement de données personnelles est une opération, ou ensemble d'opérations, portant sur
      des données personnelles, quel que soit le procédé (collecte, enregistrement, organisation,
      conservation, adaptation, modification, extraction, consultation, utilisation, communication
      par transmission ou diffusion ou toute autre forme de mise à disposition, rapprochement). Il
      n'est pas nécessairement informatisé : les fichiers papier sont également concernés et doivent
      être protégés dans les mêmes conditions.
    </p>
    <p>
      Un traitement de données personnelles doit avoir une finalité, c’est-à-dire un but utile et
      immédiatement nécessaire, qui doit bien évidemment être légal et légitime au regard de
      l’activité professionnelle.
    </p>

    <h2>Qu'est-ce que le RGPD ?</h2>
    <p>
      RGPD signifie
      <a href="https://www.cnil.fr/fr/reglement-europeen-protection-donnees">
        « Règlement Général sur la Protection des Données » </a
      >. Il s’agit d’un nouveau règlement européen qui encadre le traitement des données
      personnelles sur le territoire de l’Union européenne. Il s’inscrit dans la continuité de la
      loi française Informatique et Libertés de 1978 et renforce le contrôle par les citoyens de
      l’utilisation qui peut être faite des données les concernant. Il harmonise les règles en
      Europe en offrant un cadre juridique unique aux professionnels. Il permet de développer leurs
      activités numériques au sein de l’UE en se fondant sur la confiance des utilisateurs.
    </p>

    <h2>Qui est concerné par le RGPD ?</h2>
    <p>
      Le RGPD s’applique à toute organisation, publique ou privée, quels que soient sa taille, son
      pays d’implantation et son activité, qui traite des données personnelles pour son compte ou
      non, dès lors qu'elle est établie sur le territoire de l’Union européenne, ou que son activité
      cible directement des résidents européens. Le traitement ou collecte de données pour le compte
      d’une autre entité (entreprise, collectivité, association) implique également des obligations
      spécifiques pour garantir la protection des données confiées.
    </p>

