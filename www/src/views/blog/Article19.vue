    <p>
      Le propriétaire va chercher un nouveau locataire pour éviter que le
      logement ne soit vacant trop longtemps, il va donc certainement faire
      quelques visites avant votre départ.
    </p>
    <p>
      Même si vous êtes toujours le locataire en place, vous êtes tenu de
      laisser visiter le logement dans la limite de
      <strong>2 heures par jour ouvrable</strong>. Ainsi, il ne peut pas vous
      demander d’organiser des visites le week-end et les jours fériés, ni en
      faire toute la journée.
    </p>
    <p>
      S’il trouve un locataire avant votre départ, vous pouvez vous mettre
      d’accord pour raccourcir votre préavis.
    </p>
