    </p>
    <h2 class="fr-h6">
      👋 Étape 3 : Préparer et réaliser l'état des lieux de sortie
    </h2>
    <p>
      Après avoir donné congé au propriétaire, vous devrez préparer votre
      départ. En prenant possession des lieux, un état des lieux d’entrée a été
      réalisé. Lors du départ, un
      <a
        href="https://www.legifrance.gouv.fr/loda/id/JORFTEXT000032320564/
"
        target="_blank"
        rel="noopener"
        >état des lieux de sortie</a
      >
      doit donc être réalisé.
    </p>
    <p>
      Il faut se montrer vigilant et comparer avec l’état des lieux entrant,
      distinguer les éventuelles dégradations de ce qui relève de la vétusté.
    </p>
    <p>
      La liste des
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F31697
"
        target="_blank"
        rel="noopener"
        >réparations à la charge du locataire</a
      >
      est encadrée par décret et consultable sur
      <a
        href="https://www.legifrance.gouv.fr/loda/article_lc/LEGIARTI000006491868
"
        target="_blank"
        rel="noopener"
        >legifrance.gouv.fr</a
      >.
    </p>
    <p>
      <strong
        >Cette étape est très importante pour récupérer le dépôt de
        garantie.</strong
      >
    </p>
    <p>
      Si elles sont d'accord, les deux parties signent l’état des lieux sortant,
      sinon, il sera fait par huissier au frais du locataire et du propriétaire.
    </p>
    <h2 class="fr-h6">
      💸 Étape 4 : Récupérer sa caution après l'état des lieux de sortie
    </h2>
    <p>
      La restitution du dépôt de garantie peut intervenir le jour de l’état des
      lieux mais dans certains cas elle peut mettre jusqu’à
      <strong>deux mois.</strong>
    </p>
    <p>
      Lorsque l’état des lieux sortant est validé, que la remise des clés a été
      effectuée, le propriétaire a
      <strong>un mois pour restituer le dépôt de garantie,</strong>
      deux en cas de litige. Il est en droit de retenir les frais des
      réparations ou les éventuels impayés, mais doit justifier sur présentation
      des factures ou des lettres de relance des loyers non perçus.
    </p>
    <p>
      En revanche, le
      <strong
        >propriétaire est redevable de 10 % du loyer pour chaque mois de retard
        de la restitution du dépôt de garantie.</strong
      >
    </p>
    <p>
      En cas de défaut de remboursement de la caution dans les délais, une mise
      en demeure peut être adressée au propriétaire par courrier recommandé avec
      accusé de réception.
    </p>
    <p>
      En ultime recours, le locataire dispose d’un délai de trois ans, à compter
      de la date limite de remboursement du
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F31269#:~:text=D%C3%A9lai-,En%20fin%20de%20bail%2C%20le%20d%C3%A9p%C3%B4t%20de%20garantie%20doit%20%C3%AAtre,amput%C3%A9%20de%20retenues%20ou%20provision.
"
        target="_blank"
        rel="noopener"
        >dépôt de garantie</a
      >, pour saisir le tribunal compétent.
    </p>
    <h2 class="fr-h6">🔑 Étape 5 : Rendre les clés et déménager</h2>
    <p>
      Après avoir déménagé, il faut restituer toutes les clés au propriétaire ou
      à l’agent immobilier mandaté. La restitution marque la fin du contrat de
      location.
    </p>
