    <h2 class="fr-h6">✉️ Étape 2 : Informer son propriétaire de son départ</h2>
    <p>
      Une fois votre appartement trouvé, il faut informer votre propriétaire de
      votre souhait de mettre fin au bail, ce qu’on appelle aussi lui
      <strong>donner congé</strong>. Vous pouvez le faire à tout moment sans
      avoir à avancer de raison. Vous pouvez utiliser
      <a
        href="https://www.service-public.fr/simulateur/calcul/CongeLogement
"
        target="_blank"
        rel="noopener"
        >ce modèle</a
      >. Il faut ensuite respecter un délai de préavis avant la résiliation
      complète du bail.
    </p>
    <p>
      📅 Le délai de préavis varie en fonction du lieu et de si la location est
      vide ou meublée. Il est
      <strong>de 3 mois en général</strong> et
      <strong>1 mois en zone tendue</strong> (les zones où se loger est
      difficile et coûteux) ce qui concerne plus de 1000 communes et globalement
      toutes les grandes villes (Paris, Lyon, Bordeaux…)
    </p>
    <p>
      👉 Utiliser
      <a
        href="https://www.service-public.fr/simulateur/calcul/zones-tendues
"
        target="_blank"
        rel="noopener"
        >ce simulateur</a
      >
      pour savoir si votre logement est en zone tendue.
    </p>
    <p>
      ⚡ Attention à bien anticiper la date de votre départ et à soumettre votre
      préavis en temps et en heure, ni trop tôt ni trop tard. Si vous partez
      avant la date de fin de votre préavis, vous aurez l’<strong
        >obligation de continuer à payer le loyer et les charges</strong
      >
      après avoir quitté le logement, tout en assumant la charge de votre
      nouveau logement.
    </p>
    <p>
      Pour envoyer votre préavis au propriétaire, trois méthodes sont acceptées
      :
    </p>
    <ol>
      <li>
        Envoyer sa demande par
        <strong>lettre recommandée avec accusé de réception.</strong>
        Le préavis de location prend effet dès que le bailleur a effectivement
        pris connaissance du courrier. C’est la méthode la plus courante.
      </li>
      <li>
        <strong>Remettre en main propre</strong> contre émargement ou récépissé.
      </li>
      <li>
        <strong>Procéder par acte d’huissier.</strong> Cette méthode est rare
        car son coût est plus élevé (<a
          href="https://www.service-public.fr/particuliers/vosdroits/F32973
"
          target="_blank"
          rel="noopener"
          >comptez environ 150€</a
        >), en revanche la date du début de préavis est fixée au jour où
        l’huissier s’est présenté chez le bailleur, que celui-ci soit présent ou
        non.
      </li>
    </ol>
    <p>⚠️ Vous ne pouvez pas donner congé par un simple mail.</p>
