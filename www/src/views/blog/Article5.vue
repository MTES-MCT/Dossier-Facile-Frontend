      l’État, ce qui rassure les propriétaires ! N’hésitez plus, rejoignez la
      communauté !
    </p>
    <h2>3. Faire usage de son réseau immédiat</h2>
    <p>
      Des amis qui déménagent ? Des collègues experts en la matière ? Un
      grand-oncle fraîchement propriétaire ? N’hésitez pas à les mettre à
      contribution ! C’est souvent par eux que vous obtiendrez les meilleures
      astuces. Une simple information, dans un couloir ou à la machine à café,
      peut vous ouvrir de nombreuses portes, et vous conduire à l’appartement
      idéal. D’autant que connaître son propriétaire est un réel avantage : avec
      une relation basée sur la confiance, tout est plus facile.
    </p>
    <h2>4. Utiliser les réseaux sociaux</h2>
    <p>
      Les réseaux sociaux simplifient grandement les démarches, et ouvrent de
      nouvelles opportunités dans la recherche d’un logement. Un statut ou un
      post Facebook peuvent vous permettre d’être mis en relation avec un
      propriétaire ou un locataire qui quitte son appartement. N’hésitez pas non
      plus à rejoindre des groupes de discussion formés pour aider à la
      recherche d’un logement (exemple : “Plan appart Paris” sur Facebook). Les
      bons plans y circulent, que ce soient des commentaires utiles ou même
      parfois des offres très intéressantes.
      <br />
      Autrement dit, réalisez une veille quotidienne afin de suivre les
      opportunités et de ne pas passer à côté d’une offre en or.
    </p>
