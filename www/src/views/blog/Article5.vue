    <h2>1. Bien connaître le marché locatif parisien</h2>
    <p>
      En région parisienne, la demande d’appartements surpasse largement
      l’offre. Il est donc presque nécessaire de devenir un expert des bons
      plans et autres astuces pour trouver l’appartement qui te convient.<br />
      Il est notamment important d’avoir une idée précise des prix pratiqués sur
