    <h2>
      Combien de temps mes données sont-elles conservées chez DossierFacile ?
    </h2>
    <p>
      DossierFacile conserve vos pièces justificatives et données personnelles
      autres que nom, prénom, email, statut familial, code postal tant que votre
      compte reste actif (c’est-à-dire que vous vous connectez). Lorsque votre
      compte reste inactif plus de
      <span class="fr-text--bold">3 mois</span>, vos pièces sont supprimées et
      ce conformément au
      <a
        href="https://www.cnil.fr/sites/default/files/atoms/files/referentiel_relatif_aux_traitements_de_donnees_personnelles_mis_en_oeuvre_dans_le_cadre_de_la_gestion_locative.pdf"
        >référentiel relatif à la gestion locative</a
      >
      de la CNIL (Commission nationale de l’informatique et des libertés).
