      />
    </p>
    <h2>Le montant du loyer</h2>
    <p>
      Le montant du loyer d'un logement, qu’il soit loué vide ou meublé, est en
      principe fixé librement par le propriétaire.
    </p>
    <p>
      Toutefois, les communes situées en zone tendue sont soumises à
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F1314"
        target="_blank"
        rel="noreferrer"
        >l'encadrement des loyers</a
      >. Dans ces villes, le loyer est plafonné lors de la mise en location du
      logement (cas d'un nouveau locataire) et lors du renouvellement du bail
      (cas du même locataire).
    </p>
    <p>
      Paris, Lyon et Lille font par exemple l’objet de règles spécifiques. Un
      <a
        href="https://www.service-public.fr/simulateur/calcul/zones-tendues"
        target="_blank"
        rel="noreferrer"
        >simulateur</a
      >
      a d’ailleurs été mis en place par l’État pour vous permettre de savoir si
      votre logement est en zone tendue ou non.
    </p>
    <p>
      Pour ce qui est de la révision du loyer, le propriétaire peut
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F1311"
        target="_blank"
        rel="noreferrer"
        >réviser le loyer</a
      >
      1 fois par an à la condition qu'une clause inscrite dans le bail le
      prévoit.
    </p>
    <h2>Le versement du loyer</h2>
    <p>
      Le loyer et les charges sont à payer pendant toute la durée du bail à la
      date convenue et indiquée dans ce dernier.
    </p>
    <p>
      Lorsque le locataire paie à plusieurs reprises son loyer en retard, le
      bailleur
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F929"
        target="_blank"
        rel="noreferrer"
        >est en droit de ne pas renouveler le bail pour motif légitime et
        sérieux.</a
      >
    </p>
    <p>
      Le non-paiement ou le paiement partiel du loyer et des charges peut
      engendrer la
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F31272"
        target="_blank"
        rel="noreferrer"
        >résiliation du bail par le propriétaire et l'expulsion du locataire</a
      >, sauf si le locataire n'est pas en cause (exemple : non versement des
      aides au logement en cas de
      <a
        href="https://www.legifrance.gouv.fr/loda/id/JORFTEXT000000217471/"
        target="_blank"
        rel="noreferrer"
        >logement non décent</a
      >
      ).
    </p>
    <p>
      À savoir : aucun frais ne peut être facturé par le propriétaire en cas de
      retard de paiement du loyer. La mise en place d’une clause dans le contrat
      de location, autorisant le propriétaire à percevoir des amendes ou des
      pénalités en cas d'infraction au contrat de location ou au règlement de
      l'immeuble est abusive. Elle peut être dénoncée devant le juge des
      contentieux de la protection du
      <a
        href="https://www.justice.fr/recherche/annuaires"
        target="_blank"
        rel="noreferrer"
        >tribunal</a
      >
      dont dépend le logement.
    </p>
    <p>
      Attention ! Même si le propriétaire n'effectue pas des travaux qui lui
      incombent, le locataire doit continuer à payer le loyer et les charges !
      En revanche, il peut engager les
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F31699"
        target="_blank"
        rel="noreferrer"
        >démarches à sa disposition</a
      >. Seul un juge peut décider de
      <a
        href="https://consignations.caissedesdepots.fr/particulier/consignez-votre-loyer/consignez-votre-loyer-en-cas-de-litige-avec-votre-bailleur"
        target="_blank"
        rel="noreferrer"
        >bloquer temporairement le versement du loyer (hors charges)</a
      >
      au propriétaire, dans l'attente de la résolution du conflit.
    </p>
    <p>
      A noter également, le dépôt de garantie versé par le locataire en début de
      location ne lui permet pas de
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F31629"
        target="_blank"
        rel="noreferrer"
        >ne pas payer le dernier mois de loyer</a
      >.
    </p>
    <h2>Les moyens de paiement du loyer</h2>
    <p>
      Différents moyens de paiement peuvent être proposés au locataire qui est
      libre de refuser la proposition du propriétaire :
    </p>
    <ul>
      <li>Prélèvement automatique sur le compte bancaire du locataire ;</li>
      <li>
        <a
          href="https://www.service-public.fr/particuliers/vosdroits/F20752"
          target="_blank"
          rel="noreferrer"
          >Titre interbancaire de paiement (TIP) ;</a
        >
      </li>
      <li>Chèque ;</li>
      <li>
        Espèces, à la condition que le montant à payer soit inférieur à 1 000 €
      </li>
    </ul>

    <p>
      Des plateformes de locations immobilières digitales, mettent également en
      place des moyens de paiement de loyer sécurisés. C’est notamment le cas de
      <a href="https://pandaloc.com/" target="_blank" rel="noreferrer"
        >Pandaloc</a
      >, qui a mis en place un système de paiement 100% sécurisé et encadré par
      l’ACPR Banque de France. Ce système permet aux propriétaires de recevoir
      le loyer qui leur est dû en temps et en heure et aux locataires d’avoir
      des rappels pour effectuer le transfert mais aussi d’avoir leurs
      quittances de loyer de façon rapide et automatique.
    </p>
    <h2>La quittance de loyer</h2>
    <p>
      Le propriétaire est tenu de délivrer au locataire qui en fait la demande
      une
      <a
        href="https://www.inc-conso.fr/content/logement/vous-demandez-une-quittance-de-loyer-votre-proprietaire"
        target="_blank"
        rel="noreferrer"
        >quittance de loyer</a
      >, document qui atteste que le locataire a payé intégralement le loyer et
      les charges et ce de façon totalement gratuite. La quittance doit indiquer
      le détail des sommes versées par le locataire, en distinguant le loyer et
      les
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F947"
        target="_blank"
        rel="noreferrer"
        >charges</a
      >.<br />
      Elle est le plus souvent mensuelle mais il n’y a pas de règles spécifiques
      sur la période qu’elle couvre.
    </p>
    <p>
      A savoir : en cas de paiement partiel du loyer, le propriétaire ou
      l'agence immobilière doit remettre au locataire un reçu.
    </p>
    <h2>Difficultés à payer votre loyer ?</h2>
    <p>
      Si vous rencontrez des difficultés pour payer votre loyer (mais sans
      impayés), sachez que des solutions existent ! Vous pouvez commencer par
      essayer
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F1008"
        target="_blank"
        rel="noreferrer"
        >de trouver une solution à l'amiable avec le propriétaire</a
      >.
    </p>
    <p>
      En cas de difficultés persistantes, des
      <a
        href="https://www.service-public.fr/particuliers/vosdroits/F1008"
        target="_blank"
        rel="noreferrer"
        >aides sociales</a
      >
      existent et peuvent vous être accordées.
    </p>
